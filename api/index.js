const BASE_URL = "http://106.52.24.209:3000"
// const BASE_URL = "http://106.52.24.209:3000"

class YBRequest {
  request(url, method, params) {
    return new Promise((resolve, reject) => {
      wx.request({
        url: BASE_URL + url,
        method: method,
        data: params,
        success: function(res) {
          resolve(res.data)
        },
        fail: reject
      })
    })
  }

  get(url, params) {
    return this.request(url, "GET", params)
  }

  post(url, data) {
    return this.request(url, "POST", data)
  }
}

const ybRequest = new YBRequest()

export default ybRequest
